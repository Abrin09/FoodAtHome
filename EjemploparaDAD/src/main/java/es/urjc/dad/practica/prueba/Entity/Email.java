package es.urjc.dad.practica.prueba.Entity;

public class Email{
	
	public String name;
	public String Email;
	
	public Email(String name, String email) {
		super();
		this.name = name;
		Email = email;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEmail() {
		return Email;
	}

	public void setEmail(String email) {
		Email = email;
	}
	
	
	
	
}